package bean;

public class Course {
private String id;
private String cname;
private int cost;

public String getId() {
	return id;
}

public void setId(String id) {
	this.id = id;
}

public String getCname() {
	return cname;
}

public void setCname(String cname) {
	this.cname = cname;
}

public int getCost() {
	return cost;
}

public void setCost(int cost) {
	this.cost = cost;
}

}
